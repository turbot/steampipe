package modconfig

// ResourceMetadata :: additional data we collect about each resource to populate the reflection tables
type ResourceMetadata struct {
	ResourceName string `hcl:"resource_name" column_type:"text"`
	// mod name in the format mod.<modName>@<version?
	ModName          string `hcl:"mod_name" column_type:"text"`
	FileName         string `hcl:"file_name" column_type:"text"`
	StartLineNumber  int    `hcl:"start_line_number" column_type:"integer"`
	EndLineNumber    int    `hcl:"end_line_number" column_type:"integer"`
	IsAutoGenerated  bool   `hcl:"auto_generated" column_type:"bool"`
	SourceDefinition string `hcl:"source_definition" column_type:"text"`

	// mod short name
	ModShortName string
}

// SetMod set the mod name and mod short name
func (m *ResourceMetadata) SetMod(mod *Mod) {
	m.ModShortName = mod.ShortName
	m.ModName = mod.FullName
}

